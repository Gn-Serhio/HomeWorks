    # Есть две коробки, первая размером A1×B1×C1, вторая размером A2×B2×C2.
    # Определите, можно ли разместить одну из этих коробок внутри другой, при условии,
    # что поворачивать коробки можно только на 90 градусов вокруг ребер.
    # Формат входных данных
    # Программа получает на вход числа A1, B1, C1, A2, B2, C2.
A1, B1, C1 = int(input('Введите сторону А1 - ')), int(input('Введите сторону В1 - ')), int(input('Введите сторону С1 - '))
A2, B2, C2 = int(input('Введите сторону А2 - ')), int(input('Введите сторону В2 - ')), int(input('Введите сторону С2 - '))

# ставлю коробки в определенноеположение
if A1 > B1:
    A1, B1 = B1, A1
if B1 > C1:
    B1, C1 = C1, B1
if A1 > B1:
    A1, B1 = B1, A1

if A2 > B2:
    A2, B2 = B2, A2
if B2 > C2:
    B2, C2 = C2, B2
if A2 > B2:
    A2, B2 = B2, A2

# теперь сравниваю коробки
if A1 == A2 and B1 == B2 and C1 == C2:
    print('Коробки равны')
elif A1 > A2 and B1 > B2 and C1 > C2:
    print('Превая коробка больше и вторая в нее поместится')
elif A2 > A1 and B2 > B1 and C2 > C1:
    print('Вторая коробка больше и первая в нее поместится')
