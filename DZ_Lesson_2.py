# Даны три натуральных числа A, B, C. Определите, существует ли треугольник с такими сторонами

A, B, C = int(input('Введите 1-ю сторону треугольника - ')), int(input('Введите 2-ю сторону треугольника - ')), int(input('Введите 3-ю сторону треугольника - '))
if (A + B >= C) and (A + C >= B) and (B + C >= A):
    print('Треугольник существует')
else:
    print("Треугольника не существует")


# Задача 3 - Даны три целых числа. Определите, сколько среди них совпадающих.
    # Программа должна вывести одно из чисел:
    # 3 (если все совпадают), 2 (если два совпадает) или 0 (если все числа различны).

num1, num2, num3 = int(input('Введите 1-е число - ')), int(input('Введите 2-е число - ')), int(input('Введите 3-е число - '))
if num1 == num2 and num1 == num3:
    print('3')
elif (num1 == num2 and num1 != num3) or (num1 == num3 and num1 != num2) or (num2 == num3 and num2 != num1):
    print('2')
else:
    print('0')

